
public class DisplayVariableTypes {
	public static void main(final String[] args) {

		System.out.println("Variables in Java:\n");
		final int myNumber = 88;
		System.out.println("\tThe 'int' type: " + myNumber);

		final short myShort = 847;
		System.out.println("\tThe 'short' type: " + myShort);

		final long myLong = 9797;
		System.out.println("\tThe 'long' type: " + myLong);

		final double myDouble = 7.3243;
		System.out.println("\tThe 'double' type: " + myDouble);

		final float myFloat = 3.14f; // pi
		System.out.println("\tThe 'float' type: " + myFloat);	         

		final char myChar = 'y';
		System.out.println("\tThe 'char' type: " + myChar);

		final boolean myBoolean = false;
		System.out.println("\tThe 'boolean' type: " + myBoolean);

		final byte myByte = 127;
		System.out.println("\tThe 'byte' type: " + myByte);
		
		System.out.println("\nEnd of program!");

	}
}
